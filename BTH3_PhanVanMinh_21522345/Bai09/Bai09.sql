    MaSV INT PRIMARY KEY,
	MaNganh VARCHAR(10) FOREIGN KEY REFERENCES NganhHoc(MaNganh),
    HoTen NVARCHAR(50) NOT NULL,
    GioiTinh CHAR NOT NULL,
)
